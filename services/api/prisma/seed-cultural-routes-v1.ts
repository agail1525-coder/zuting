  // ── 1. 查询 religionId 映射 ──
  const religions = await prisma.religion.findMany({ select: { id: true, slug: true } });
  const religionMap: Record<string, string> = {};
  for (const r of religions) religionMap[r.slug] = r.id;

  // ── 2. 查询所有圣地 (按 name 索引) ──
  const allSites = await prisma.holySite.findMany({
    select: { id: true, name: true, imageUrl: true, religion: { select: { slug: true } } },
  });
  const siteIdByName: Record<string, string> = {};
  const poolBySlug: Record<string, string[]> = {};
  for (const s of allSites) {
    siteIdByName[s.name] = s.id;
    const slug = s.religion?.slug;
    if (slug && s.imageUrl) {
      (poolBySlug[slug] ||= []).push(s.imageUrl);
    }
  }

  console.log(`\n[info] ${religions.length} religions, ${allSites.length} holy sites loaded`);

  // ── 3. 逐路线 upsert ──
  for (const spec of ROUTES) {
    console.log(`\n[route] ${spec.slug} (${spec.duration} days, ${spec.religionSlug})`);
    const religionId = religionMap[spec.religionSlug];
    if (!religionId) {
      console.warn(`  ⚠ religion not found: ${spec.religionSlug}, skip`);
      continue;
    }

    // coverImage: 取该宗池首张图
    const pool = poolBySlug[spec.religionSlug] || [];
    const coverImage = pool[hashStr(spec.slug) % Math.max(pool.length, 1)] || null;
    const images = pool.slice(0, 12);

    const routeData = {
      slug: spec.slug,
      title: spec.title,
      titleEn: spec.titleEn,
      subtitle: spec.subtitle,
      category: spec.category,
      difficulty: spec.difficulty,
      duration: spec.duration,
      nights: spec.nights,
      coverImage,
      images,
      highlights: spec.highlights,
      description: spec.descriptionParagraphs.join('\n'),
      itinerary: spec.itinerary as unknown as Prisma.InputJsonValue,
      priceFrom: spec.priceFrom,
      included: spec.included,
      excluded: spec.excluded,
      tips: spec.tips,
      season: spec.season,
      groupSize: spec.groupSize,
      status: RouteStatus.PUBLISHED,
      rating: spec.rating,
      reviewCount: spec.reviewCount,
      bookCount: spec.bookCount,
      priceMode: spec.priceMode,
      relatedPatriarchs: spec.relatedPatriarchs as unknown as Prisma.InputJsonValue,
      relatedTeachings: spec.relatedTeachings as unknown as Prisma.InputJsonValue,
      religionId,
    };

    const existing = await prisma.route.findUnique({ where: { slug: spec.slug } });
    let routeId: string;
    if (existing) {
      await prisma.route.update({ where: { slug: spec.slug }, data: routeData });
      routeId = existing.id;
      await prisma.routeSite.deleteMany({ where: { routeId } });
      console.log(`    ↻ update route ${spec.slug}`);
    } else {
      const r = await prisma.route.create({ data: routeData });
      routeId = r.id;
      console.log(`    ✚ create route ${spec.slug}`);
    }

    // bind sites
    let bindingCount = 0;
    for (const b of spec.bindings) {
      const siteId = siteIdByName[b.siteName];
      if (!siteId) {
        console.log(`    ⚠ site not found: ${b.siteName}`);
        continue;
      }
      await prisma.routeSite.create({
        data: { routeId, siteId, day: b.day, order: b.order, duration: b.duration, note: b.note },
      });
      bindingCount++;
    }
    console.log(`    ✓ ${bindingCount} sites bound`);
  }

  console.log('\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
