async function main() {
  console.log('🌤  目的地++ bestSeason 气候回填\n');

  const sites = await prisma.holySite.findMany({
    where: { bestSeason: null },
    select: { id: true, name: true, country: true },
  });

  let updated = 0;
  let noMap = 0;
  const missingCountries = new Set<string>();

  for (const s of sites) {
    const season = SEASON_BY_COUNTRY[s.country];
    if (!season) {
      noMap++;
      missingCountries.add(s.country);
      continue;
    }
    await prisma.holySite.update({ where: { id: s.id }, data: { bestSeason: season } });
    updated++;
  }

  console.log(`✓ 回填: ${updated}`);
  console.log(`⚠ 国家未映射: ${noMap}`);
  if (missingCountries.size) console.log(`  缺映射国家: ${[...missingCountries].join(', ')}`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
