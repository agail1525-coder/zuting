async function seedFamilyHarmony() {
  console.log('👨‍👩‍👧‍👦 Seeding family-harmony themes...');

  for (const theme of FAMILY_THEMES) {
    await prisma.teamCultureTheme.upsert({
      where: { slug: theme.slug },
      update: {
        title: theme.title,
        subtitle: theme.subtitle,
        description: theme.description,
        color: theme.color,
        icon: theme.icon,
        coverUrl: theme.coverUrl,
        keywords: theme.keywords,
        holySites: theme.holySites,
        routes: theme.routes,
        rituals: theme.rituals as never,
        richContent: theme.richContent as never,
        priceFrom: theme.priceFrom,
        durationDays: theme.durationDays,
        sortOrder: theme.sortOrder,
        category: 'FAMILY',
        isPublished: true,
      },
      create: {
        slug: theme.slug,
        title: theme.title,
        subtitle: theme.subtitle,
        description: theme.description,
        color: theme.color,
        icon: theme.icon,
        coverUrl: theme.coverUrl,
        keywords: theme.keywords,
        holySites: theme.holySites,
        routes: theme.routes,
        rituals: theme.rituals as never,
        richContent: theme.richContent as never,
        priceFrom: theme.priceFrom,
        durationDays: theme.durationDays,
        sortOrder: theme.sortOrder,
        category: 'FAMILY',
        isPublished: true,
      },
    });
    console.log(`  ✅ ${theme.title} (${theme.slug})`);
  }

  console.log(`👨‍👩‍👧‍👦 Family harmony: ${FAMILY_THEMES.length} themes seeded`);
}

// 支持独立执行和被导入调用
if (require.main === module) {
  seedFamilyHarmony()
    .then(() => prisma.$disconnect())
    .catch((e) => {
      console.error(e);
      prisma.$disconnect();
      process.exit(1);
    });
}

export { seedFamilyHarmony };
