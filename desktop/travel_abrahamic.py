# 便捷查询函数
def get_guide(site_name: str) -> dict:
    """根据圣地名称获取旅行攻略"""
    if site_name in TRAVEL_GUIDES_ABRAHAMIC:
        return TRAVEL_GUIDES_ABRAHAMIC[site_name]
    # 模糊搜索
    matches = [k for k in TRAVEL_GUIDES_ABRAHAMIC if site_name in k]
    if matches:
        return {k: TRAVEL_GUIDES_ABRAHAMIC[k] for k in matches}
    return {}


def get_sites_by_religion(religion: str) -> list:
    """按宗教筛选圣地列表
    religion: '基督教' | '伊斯兰教' | '犹太教'
    """
    # 基于key中的地理和名称特征分类
    christian_keywords = ["教堂", "大教堂", "大殿", "修道院", "教会", "洞穴教堂", "各各他", "晚餐楼", "安提阿"]
    islamic_keywords = ["清真寺", "圣陵", "陵墓", "绿穹", "阿尔罕布拉", "希拉山洞", "梅夫拉纳"]
    jewish_keywords = ["会堂", "犹太", "西墙", "哭墙", "马萨达", "死海", "锡安山", "先祖墓",
                       "大卫城", "圣殿山", "奥斯维辛", "学院遗址", "库姆兰", "伯尔谢巴",
                       "拉结墓", "橄榄山犹太", "雅法门", "提比利亚", "采法特"]

    if religion == "基督教":
        keywords = christian_keywords
    elif religion == "伊斯兰教":
        keywords = islamic_keywords
    elif religion == "犹太教":
        keywords = jewish_keywords
    else:
        return []

    return [k for k in TRAVEL_GUIDES_ABRAHAMIC if any(kw in k for kw in keywords)]


def count_sites() -> dict:
    """统计各宗教圣地数量"""
    return {
        "总计": len(TRAVEL_GUIDES_ABRAHAMIC),
        "基督教": len(get_sites_by_religion("基督教")),
        "伊斯兰教": len(get_sites_by_religion("伊斯兰教")),
        "犹太教": len(get_sites_by_religion("犹太教")),
    }


